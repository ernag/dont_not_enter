#include "sleep_timer.h"
#include "corona_console.h"
extern cc_handle_t g_cc_handle;


////////////////////////////////////////////////////////////////////////////////
// Code
////////////////////////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////////////////////
//! \brief Simple test to see if we can SLEEP properly.
//!
//! \fntype Reentrant Function
//!
//! \detail This function tries to sleep for 5 seconds.  It is up to the user
//! to measure and see if it really worked.  It uses the FTM module.
//!
//! \return corona_err_t error code
//!
///////////////////////////////////////////////////////////////////////////////
corona_err_t sleep_timer_test(uint32_t seconds)
{
  cc_print(&g_cc_handle, "sleeping %d ms...\n", seconds * 1000);
  sleep(seconds * 1000);
  cc_print(&g_cc_handle, "done\n");
  return SUCCESS;
}

